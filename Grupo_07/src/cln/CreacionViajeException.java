package cln;

/**
 * La clase CreacionViajeException representa una excepción que se lanza
 * cuando ocurre un problema durante la creación de un objeto Viaje.
 */
public class CreacionViajeException extends Exception {
    
	/**
     * Constructor vacío de la excepción CreacionViajeException ya que hereda de la SuperClass Exception.
     * Se utiliza para crear una nueva instancia de CreacionViajeException.
     */
	public CreacionViajeException() {
		super();
	}
}
